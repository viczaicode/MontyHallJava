    private void setupUI() {
        setTitle("Monty Hall J√°t√©k");
        setSize(1000, 600);
        setLayout(new FlowLayout());

        doorModel[] ajtok = controller.getDoors();
        
        

        for (int i = 0; i < ajtok.length; i++) {
            doorModel ajto = ajtok[i];
            ImageIcon icon = new ImageIcon(ajto.getImagePath().toString());

            JLabel label = new JLabel(icon);
            
             //Kattint√°s esem√©ny
            label.addMouseListener(new MouseAdapter() {
                @Override
                public void mouseClicked(MouseEvent e) {
                    if (ajto.getPrize() == 1) {
                        label.setIcon(new ImageIcon("src/view/kepek/nyitottajto.png"));
                        JOptionPane.showMessageDialog(null, "üéâ Gratul√°lok, nyert√©l egy aut√≥t!");
                    } else {
                        label.setIcon(new ImageIcon("src/view/kepek/nyitottajto.png"));
                        JOptionPane.showMessageDialog(null, "üòÖ Ez most egy kecske volt!");
                    }
                }
            });

            doorLabels[i] = label;
            add(label);
        }

        setVisible(true);
    }
    
    

